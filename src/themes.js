// Apply theme to CSS variables
export function applyTheme(themeId) {
    const theme = themes[themeId];
    if (!theme) {
        console.error(`Theme "${themeId}" not found. Using default.`);
        return;
    }

    const root = document.documentElement;

    // Apply all color variables
    Object.entries(theme.colors).forEach(([key, value]) => {
        root.style.setProperty(`--${key}`, value);
    });

    // Apply background gradient
    document.body.style.background = `linear-gradient(135deg, ${theme.colors['bg-dark-1']} 0%, ${theme.colors['bg-dark-2']} 50%, ${theme.colors['bg-dark-3']} 100%)`;

    console.log(`Theme applied: ${theme.name}`);
}

// Get current theme from localStorage
export function getCurrentTheme() {
    return localStorage.getItem('ndt-theme') || 'cyber-teal';
}

// Save theme to localStorage
export function saveTheme(themeId) {
    localStorage.setItem('ndt-theme', themeId);
    applyTheme(themeId);
}

// Initialize theme on app load
export function initializeTheme() {
    const currentTheme = getCurrentTheme();
    applyTheme(currentTheme);
}

export default {
    themes,
    applyTheme,
    getCurrentTheme,
    saveTheme,
    initializeTheme
};
